const noGifs = [
    "public/gif/cat-kiss.gif",
    "public/gif/heart-break.gif",
    "public/gif/cry.gif",
    "public/gif/cry-at-screen.gif",
];

let currentGifIndex = 0;

function setRandomNoGif() {
    const img = document.querySelector('.gif_container img');
    let randomIndex;
    do {
        randomIndex = Math.floor(Math.random() * noGifs.length);
    } while (randomIndex === currentGifIndex && noGifs.length > 1);

    currentGifIndex = randomIndex;
    img.src = noGifs[randomIndex];
}

const messages = [
    "Are you sure?",
    "Really sure??",
    "Are you positive?",
    "Hayate please...",
    "Just think about it!",
    "If you say no, I will be really sad...",
    "I will be very sad...",
    "I will be very very very sad...",
    "Ok fine, I will stop asking...",
    "Just kidding, say yes please! ❤️"
];

let messageIndex = 0;

function handleNoClick() {
    const noButton = document.querySelector('.no-button');
    const yesButton = document.querySelector('.yes-button');
    noButton.textContent = messages[messageIndex];
    messageIndex = (messageIndex + 1) % messages.length;
    const currentSize = parseFloat(window.getComputedStyle(yesButton).fontSize);
    yesButton.style.fontSize = `${currentSize * 1.5}px`;
    setRandomNoGif();
}

function handleYesClick() {
    window.location.href = "yes_page.html";
}


// Import the data to customize and insert them into page
const fetchData = () => {
  fetch("customize.json")
    .then((data) => data.json())
    .then((data) => {
      Object.keys(data).map((customData) => {
        if (data[customData] !== "") {
          if (customData === "imagePath") {
            document
              .getElementById(customData)
              .setAttribute("src", data[customData]);
          } else {
            document.getElementById(customData).innerText = data[customData];
          }
        }
      });
    })
    .catch((error) => {
      console.log("Could not load customize.json, using default values");
      // Continue with default values already in HTML
    });
};

// Run fetch and animation in sequence
const resolveFetch = () => {
  return new Promise((resolve, reject) => {
    fetchData();
    resolve("Fetch done!");
  });
};

resolveFetch().then(animationTimeline());